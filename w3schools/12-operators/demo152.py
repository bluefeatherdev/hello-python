# W3Schools, Python Operators

# Operator Precedence
print((6 + 3) - (6 + 3))    # 1. Parentheses
print(100 - 3 ** 3)         # 2. Exponentiation
print(100 + ~3)             # 3. Unary plus, unary minus, and bitwise NOT
print(100 + 5 * 3)          # 4. Multiplication, division, floor division, and modulus
print(100 - 5 * 3)          # 5. Addition and subtraction
print(8 >> 4 - 2)           # 6. Bitwise left and right shifts
print(6 & 2 + 1)            # 7. Bitwise AND
print(6 ^ 2 + 1)            # 8. Bitwise XOR
print(6 | 2 + 1)            # 9. Bitwise OR
print(5 == 4 + 1)           # 10. Comparisons, identity, and membership operators
print(not 5 == 5)           # 11. Logical NOT
print(1 or 2 and 3)         # 12. AND
print(4 or 5 + 10 or 8)     # 13. OR
