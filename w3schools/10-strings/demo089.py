# W3Schools, Python Strings, Escape Characters

# Single Quote
txt = 'It\'s alright.'
print(txt)

# Backslash
txt = "This will insert one \\ (backslash)."
print(txt)

# New Line
txt = "Hello\nWorld!"
print(txt)

# Carriage Return
txt = "Hello\rWorld!"
print(txt)

# Tab (Tabulation)
txt = "Hello\tWorld!"
print(txt)

# Backspace
txt = "Hello \bWorld!"
print(txt)

# Octal value
txt = "\110\145\154\154\157"
print(txt)

# Hex value
txt = "\x48\x65\x6c\x6c\x6f"
print(txt)
